gulp.task('server', () => {
  nodemon({
    script: 'bin/www',
    watch: [
      'bin',
      'config',
      'database',
      'models',
      'routes',
      'services',
      'utils',
      'main.js'
    ]
  });
});
gulp.task('default', ['server']);
