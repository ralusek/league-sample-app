const gulp = require('gulp');
const nodemon = require('gulp-nodemon');



/******************************************************************************/
/**********************************  SETUP  ***********************************/
/******************************************************************************/


/******************************************************************************/
/**********************************  TASKS  ***********************************/
/******************************************************************************/


/******************************************************************************/
/******************************  GULP TASKS LIST ******************************/
